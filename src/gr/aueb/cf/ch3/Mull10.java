package gr.aueb.cf.ch3;

/** υπολογιζει το γινομενο μεχρι το 10
 * με επαναλητπικο τροπο
 */

public class Mull10 {
    public static void main(String[] args) {
        int i =1;
        int mul = 1;

        while (i<=10); {
            mul = mul *i;
            i++;
            System.out.println(mul);
        }

    }
}
